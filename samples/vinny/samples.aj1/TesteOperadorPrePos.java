class TesteOperadorPrePos {
	public static void main(String[] args){
		
		//
		int x = 5;
		System.out.println("x = " + x++);		

		//
		int y = 5;
		System.out.println("y = " + y--);		

		//
		int w = 5;
		System.out.println("w = " + ++w);		

		//
		int z = 5;
		System.out.println("z = " + --z);		
	}
}