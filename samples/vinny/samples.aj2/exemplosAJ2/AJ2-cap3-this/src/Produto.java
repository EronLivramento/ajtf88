public class Produto {
	private String marca;

	public String getMarca() {
		return marca;
	}	
	public void setMarca(String marca) {
		this.marca = marca;
	}
	/*
	 * utilizando this, indica-se que o atributo marca da 
	 * classe Produto (this.marca)
	 * irá receber o parâmetro marca, que possui o mesmo nome do atributo
	 */
}
